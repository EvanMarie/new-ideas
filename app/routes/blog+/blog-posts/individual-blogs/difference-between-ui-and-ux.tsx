        <p>
          User Experience (UX) refers to the overall experience a user has while
          interacting with a product. This includes their emotions, perceptions,
          and responses before, during, and after use. UX design is concerned
          with the usability, functionality, and overall satisfaction of the
          user journey.
        </p>

